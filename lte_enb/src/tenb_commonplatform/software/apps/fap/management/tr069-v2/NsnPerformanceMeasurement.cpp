u32 NsnPm::CollateResults( bool IsReqAfterReboot )
{
   
    // KPIs are collected and written to flash at times aligned with actual clock time.
    // However, it takes a few tens of millisecs between the timer going off and the KpiGroup's
    // being read, at which point they are touched with current time.  So the timer goes off
    // at 11:00:00.000 and each KPI group is touched with times a few tens of millisecs
    // after this, 11:00:00.053 say.  So when the KPIs are requested, the previous period of
    // say 10:00:00-11:00:00 doesn't include the latest KPIs.
    //
    // The offset below of a few secs takes account of this and ensures that when we aggregate
    // KPIs we will include those KPI groups that were recorded a fraction of a second after the
    // nominal reporting period.

    TimeWrap now;
    u32 numKpiGroupsAggregated;
    time_t secondsInReportDuration = m_perodicUploadInterval;
    time_t nowInUnixTime = now.GetUnixTime();
    time_t endOfLastReportDurationInUnixTime;
    time_t startOfLastReportDurationInUnixTime;
    TimeStructure timeStructure = now.GetTimeStructure();
    u32 minsAdj;
    u32 secsAdj;
    u32 phaseShiftInSecs;

    if(IsReqAfterReboot)
    {
       phaseShiftInSecs = (24*60) - ((timeStructure.theHour*60)+timeStructure.theMinutes);
       minsAdj = phaseShiftInSecs % (m_perodicUploadInterval/60);
       minsAdj = (timeStructure.theSeconds>0)? (minsAdj - 1) : minsAdj;
       secsAdj = (timeStructure.theSeconds>0)? (60-timeStructure.theSeconds) : 0;
       phaseShiftInSecs = (minsAdj*60) + secsAdj;
       phaseShiftInSecs += (m_perodicUploadPhaseAllignInMin*60);
       
       TRACE_PRINTF("Special Case: Collection Request After reboot TimeAdj is %d", phaseShiftInSecs);
       
       endOfLastReportDurationInUnixTime =  (nowInUnixTime + phaseShiftInSecs) - secondsInReportDuration;
       startOfLastReportDurationInUnixTime = (endOfLastReportDurationInUnixTime - secondsInReportDuration);
    }
    else
    {
       endOfLastReportDurationInUnixTime = ((nowInUnixTime / secondsInReportDuration) * secondsInReportDuration);
       startOfLastReportDurationInUnixTime = (endOfLastReportDurationInUnixTime - secondsInReportDuration);
    }
    
    m_endOfLastReportDuration.Set(endOfLastReportDurationInUnixTime,0);
    m_startOfLastReportDuration.Set(startOfLastReportDurationInUnixTime,0);

    numKpiGroupsAggregated = AggregateKpis(m_startOfLastReportDuration, m_endOfLastReportDuration);

    return numKpiGroupsAggregated;
}

void NsnPm::GenerateFile( const Filename & filename )
{
   TRACE_PRINTF("%s ",__FUNCTION__);
   // File deliberately not deleted.
   FILE * file = fopen(filename.c_str(), "w" );
   std::string swVersionStr,vendor;
   std::vector<u32> cellIds;
   u32 cid;
   Tr069Application::GetInstance().GetMibCache().GetMibAttribute( MibDN::GenerateMibDN(MIB_OBJECT_CLASS_FAP),PARAM_ID_TR069_SW_VERSION_STRING, swVersionStr);

   Tr069Application::GetInstance().GetMibCache().GetMibAttribute(MibDN::GenerateMibDN(MIB_OBJECT_CLASS_COMMISSIONING),PARAM_ID_GATEWAY_VENDOR, vendor);
   if(file)
   {
      string startTimeString;
      u8 subSecondDecimalPlaces = 3,numCells=1;
      string baseName = filename.GetNotDir().GetBasename();
      string fapId = baseName.substr( 0, baseName.find_first_of('_'));
      string numCellStr = getConfigParam("OAM_NUM_CELL_ENTRIES", "");

      if(NULL != numCellStr.c_str())
      {
         numCells = atoi(numCellStr.c_str());
      }
      for(u32 idx=0;idx<numCells;idx++)
      {
         Tr069Application::GetInstance().GetMibCache().GetMibAttribute(MibDN::GenerateMibDN(MIB_OBJECT_CLASS_FAP_LTE,idx),PARAM_ID_LTE_CELL_IDENTITY, cid);
         cellIds.push_back(cid);
      }
  
      fprintf(file,\
            "<?xml version=\"1.0\" encoding=\"utf-8\"?>"\
            "\n<?xml-stylesheet href=\"mine.xsl\" type=\"text/xsl\" ?>"\
            "\n<measCollecFile>"\
            "\n\t<fileheader FileFormatVersion=\"32.435 v9.0.0\" vendorName=\"%s\">"\
            "\n\t\t<fileSender elementType=\"HeNB\" />"\
            "\n\t\t<measCollec beginTime=\"%s\"/>"\
            "\n\t</fileheader>"\
            "\n\t<measData>"\
            "\n\t\t<managedElement localDn=\"ManagedElement=HENB\" swVersion=\"%s\" />"\
            ,vendor.c_str(),Tr069Application::GetInstance().GetFormattedLocalTime( startTimeString, m_startOfLastReportDuration, subSecondDecimalPlaces ).c_str() ,swVersionStr.c_str());

      for( u32 indx=0;indx<NsnPm::m_pmListLength ;indx++)
      {
         KpiGroupId kpiGroupId;
         bool kpiGroupIdFound = KpiGroup::GetKpiGroupId(m_pmList[indx].kpiId, kpiGroupId);
         if(!kpiGroupIdFound)  
         {       
            TRACE_PRINTF_CONSOLE("groupid not found");
            continue;
         }

         KpiGroupInfo::iterator innerIt;
         KpiGroups::iterator outerIt;

         outerIt = m_kpiGroups.find(kpiGroupId);   

         if (outerIt != m_kpiGroups.end())
         {
            for ( innerIt = (*outerIt).second.begin(); innerIt != (*outerIt).second.end(); innerIt++)
            {
               TimeWrap timestamp;
               (*innerIt).second.GetTimestamp(timestamp);
               fprintf(file,"\n\t\t<measInfo>\n\t\t\t<measTypes>%s</measTypes>",m_pmList[indx].pmName);
               fprintf(file,\
                     "\n\t\t\t<granPeriod endTime=\"%s\" duration=\"%dS\" />\n\t\t\t<measValue>",\
                       Tr069Application::GetInstance().GetFormattedLocalTime( startTimeString, timestamp,\
                       subSecondDecimalPlaces ).c_str(),m_collectionPeriodInSecs);
               for (u32 cell_index = 0; cell_index < numCells; cell_index++) { 
                  fprintf(file, "\n\t\t\t<measValue cellId = %d>\n\t\t\t\t<measResult>%d</measResult>",\
                        cellIds[cell_index], (*innerIt).second.GetIntTypKpi(m_pmList[indx].kpiId,cellIds[cell_index]));
               }
               fprintf(file, "\n\t\t\t</measValue>\n\t\t</measInfo>");
            }/*Inner loop*/
         }
      }

      fprintf(file,"\n\t</measData>");
      fprintf(file,\
            "\n\t<fileFooter>"\
            "\n\t\t<measCollec endTime=\"%s\" />"\
            "\n\t</fileFooter>"\
            "\n</measCollecFile>",\
            Tr069Application::GetInstance().GetFormattedLocalTime( startTimeString, m_endOfLastReportDuration, subSecondDecimalPlaces ).c_str() );

      fflush(file);
      fclose(file);
   }
   else
   {
      string generatePmDataFile = "echo Generation_of_PM_Data_file_IO_error > " + filename;

      TRACE_PRINTF("%s", generatePmDataFile.c_str());
      if( system( generatePmDataFile.c_str() ) != oam_ret_zero)
      {
         TRACE_MSG("NsnPm::GenerateFile, System command to echo Generation_of_PM_Data_file_IO_error Failed");
      }
   }

}

