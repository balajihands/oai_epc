class TransactionSessionCommand : public tr069::Tr069QueueableEvent
{
public:

    TransactionSessionCommand( shared_ptr<Tr069QueueableEvent> event, Tr069QueueableEvent::Handler dst = CWMP_CONNECTION_FSM ) :
        m_event(event),
        tr069::Tr069QueueableEvent( TR069_EVENT(TRANSACTION_SESSION_COMMAND), dst )
        {
            RSYS_ASSERT(event);
        }

    virtual ~TransactionSessionCommand(){}

    shared_ptr<Tr069QueueableEvent> GetEvent() const { return m_event; }

private:
    shared_ptr<Tr069QueueableEvent> m_event;
};

