shared_ptr<TenpinHardware> TenpinHardware::s_instance;

///////////////////////////////////////////////////////////////////////////////
// Class Functions
///////////////////////////////////////////////////////////////////////////////

TenpinHardware& TenpinHardware::GetInstance()
{
    if (NULL == s_instance)
    {
        s_instance.reset(new TenpinHardware());
    }

    return (*s_instance);
}

shared_ptr<ITenpinTpm> TenpinHardware::GetTpm()
{
    return NullTpm::GetInstance();
}
