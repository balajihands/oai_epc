///////////////////////////////////////////////////////////////////////////////
// Static Data
///////////////////////////////////////////////////////////////////////////////

shared_ptr<TenpinX509> TenpinX509::s_instance;

///////////////////////////////////////////////////////////////////////////////
// Class Functions
///////////////////////////////////////////////////////////////////////////////

TenpinX509& TenpinX509::GetInstance()
{
    if (NULL == s_instance)
    {
        s_instance.reset(new TenpinX509());
    }

    return (*s_instance);
}

shared_ptr<ITenpinX509> TenpinX509::GetX509()
{
    return OpensslX509::GetInstance();
}
