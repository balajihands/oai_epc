extern "C" CppUnit::Test* GetTests();
CppUnit::Test* GetTests()
{
    CppUnit::TestSuite* suite = new CppUnit::TestSuite("FSM unit tests");

    suite->addTest( new StateTransitions() );
    suite->addTest( new GuardConditions() );
    suite->addTest( new Actions() );
    suite->addTest( new DeepHistory() );
    suite->addTest( new ShallowHistory() );
    suite->addTest( new DeferredEvents() );

    return suite;
}


