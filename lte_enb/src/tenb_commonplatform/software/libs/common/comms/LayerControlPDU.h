///////////////////////////////////////////////////////////////////////////////

#include <comms/PDU.h>

///////////////////////////////////////////////////////////////////////////////
// Local Includes
///////////////////////////////////////////////////////////////////////////////

namespace threeway
{

///////////////////////////////////////////////////////////////////////////////
// Functions / Classes
///////////////////////////////////////////////////////////////////////////////

class LayerControlPDU : public PDU
{
public:
    enum LayerControlEvent
    {
        LAYER_OPERATIONAL = 0,
        LAYER_NOT_OPERATIONAL,
        LAYER_ERROR
    };

    LayerControlPDU(LayerControlEvent event) : m_event(event) {};
    virtual ~LayerControlPDU() {};

    /**
     * Retrieve the event.
     */
    LayerControlEvent GetEvent() const { return m_event; };

private:
    LayerControlEvent m_event;

};

}

#endif
