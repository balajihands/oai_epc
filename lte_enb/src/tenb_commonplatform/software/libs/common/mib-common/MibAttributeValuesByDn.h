#ifndef __MibAttributeValuesByDn_h_
#define __MibAttributeValuesByDn_h_

///////////////////////////////////////////////////////////////////////////////
// System Includes
///////////////////////////////////////////////////////////////////////////////

#include <sstream>

///////////////////////////////////////////////////////////////////////////////
// Local Includes
///////////////////////////////////////////////////////////////////////////////

#include "MibDN.h"
#include "MibAttributeValues.h"

///////////////////////////////////////////////////////////////////////////////
// Classes
///////////////////////////////////////////////////////////////////////////////

class MibAttributeValuesByDn : public std::map<MibDN, MibAttributeValues>
{
public:
    std::string ToString() const
    {
        std::ostringstream stream;

        for(const_iterator iter = begin();
            iter != end();
            ++iter)
        {
            stream << iter->first.ToString() << ": " << iter->second.ToString() << std::endl;
        }

        return stream.str();
    }

protected:

private:

};

#endif
