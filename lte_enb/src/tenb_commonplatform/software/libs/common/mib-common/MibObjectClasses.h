#ifndef __MibObjectClasses_h_
#define __MibObjectClasses_h_

///////////////////////////////////////////////////////////////////////////////
// System Includes
///////////////////////////////////////////////////////////////////////////////

#include <sstream>
#include <set>
#include <MibObjectClass.h>

///////////////////////////////////////////////////////////////////////////////
// Local Includes
///////////////////////////////////////////////////////////////////////////////

#include "MibAttributeDescriptors.h"

///////////////////////////////////////////////////////////////////////////////
// Classes
///////////////////////////////////////////////////////////////////////////////

class MibObjectClasses : public std::set<MibObjectClass>
{
public:

    std::string ToString() const
    {
        std::ostringstream stream;

        for(const_iterator iter = begin();
            iter != end();
            ++iter)
        {
            stream << MibObjectClassToString(*iter) << " ";
        }

        return stream.str();
    }

protected:

private:

};

#endif
