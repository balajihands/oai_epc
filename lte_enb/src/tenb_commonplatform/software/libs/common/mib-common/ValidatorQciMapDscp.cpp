#include "ValidatorQciMapDscp.h"
#define DSCP_ECN_BITS 0x03
using namespace std;

///////////////////////////////////////////////////////////////////////////////
// Class Functions
///////////////////////////////////////////////////////////////////////////////

bool ValidatorQciMapDscp::ValidateU32(u32 value, u32 min, u32 max, ValidationFailureDescriptor& failureDescriptor)
{
   if((!ValidatorU32::ValidateU32(value, min, max, failureDescriptor))||(value & DSCP_ECN_BITS))
   {
       ostringstream description;
       description << "Element :" << failureDescriptor.GetDescription();
       failureDescriptor.Set(failureDescriptor.GetCause(), description.str());
       return false;
   }

    return true;
}






