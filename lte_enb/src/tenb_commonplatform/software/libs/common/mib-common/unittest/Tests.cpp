#include <cppunit/TestSuite.h>

///////////////////////////////////////////////////////////////////////////////
// Local Includes
///////////////////////////////////////////////////////////////////////////////

#include "MibAttributeDescriptorsTest.h"
#include "MibMessageTests.h"
#include "MibValidationTests.h"
#include "MibDnTests.h"

///////////////////////////////////////////////////////////////////////////////
// Functions
///////////////////////////////////////////////////////////////////////////////

extern "C" CppUnit::Test* GetTests();
CppUnit::Test* GetTests()
{
    CppUnit::TestSuite* suite = new CppUnit::TestSuite("Common MIB library unit tests");

    suite->addTest( new MibAttributeDescriptorsTest() );
    suite->addTest( new MibMessageTests() );
    suite->addTest( new MibValidationTests() );
    suite->addTest( new MibDnTests() );

    return suite;
}
