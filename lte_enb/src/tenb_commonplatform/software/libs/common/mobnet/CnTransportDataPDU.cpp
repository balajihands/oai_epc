CnTransportDataPDU::CnTransportDataPDU() :
    SimplePDU(),
    m_domain(TOTAL_NUM_OF_DOMAIN)
{
    ENTER();
    EXIT();
}

CnTransportDataPDU::CnTransportDataPDU(DomainIdent domain) :
    SimplePDU(),
    m_domain(domain)
{
    ENTER();
    EXIT();
}

CnTransportDataPDU::~CnTransportDataPDU()
{
    ENTER();
    EXIT();
}

std::string CnTransportDataPDU::ToString() const
{
    ENTER();

    std::ostringstream stream;

    stream << SimplePDU::ToString()
           << ", domain=" << MobnetTypes::DomainToString(m_domain);

    RETURN(stream.str());
}

