///////////////////////////////////////////////////////////////////////////////
// Functions
///////////////////////////////////////////////////////////////////////////////

extern "C" CppUnit::Test* GetTests();
CppUnit::Test* GetTests()
{
    CppUnit::TestSuite* suite = new CppUnit::TestSuite("Common mobnet library unit tests");

    suite->addTest( new IMSITest() );
    suite->addTest( new FapIdTest() );
    suite->addTest( new PlmnIdTest() );
    suite->addTest( new IMEITest() );
    suite->addTest( new GtpTest() );
    suite->addTest( new UeIdentityTrackerTest() );

    return suite;
}
