TimerExpiry::TimerExpiry(const TimerHandle timer) :
    m_timerHandle(timer)
{
    SetDomain(Queueable::TIMER_EXPIRY);
}

std::string TimerExpiry::ToString() const
{
    std::ostringstream stream;

    stream << "TimerExpiry: Handle=" << m_timerHandle;

    return(stream.str());
}

