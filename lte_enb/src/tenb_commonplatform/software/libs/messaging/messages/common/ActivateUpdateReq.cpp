s32 ActivateUpdateReq::Serialise(u8* data, u32 dataMaxBytes) const
{
    ENTER();


    // Serialise all the members.
    u8* serialisedData = data;

    SerialiseIt(m_dummyPayload, serialisedData);

    RETURN(serialisedData - data);
}

bool ActivateUpdateReq::DeSerialise(const u8* data, u32 dataLen)
{
    ENTER();

    DeSerialiseIt(m_dummyPayload, data);

    RETURN(true);
}

std::string ActivateUpdateReq::ToString() const
{
    ENTER();

    ostringstream stream;

    stream << "ActivateUpdate " ;

    RETURN(stream.str());
}
