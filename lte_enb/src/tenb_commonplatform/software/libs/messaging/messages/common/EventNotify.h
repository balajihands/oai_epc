#ifndef __EventNotify_h_
#define __EventNotify_h_

///////////////////////////////////////////////////////////////////////////////
// System Includes
///////////////////////////////////////////////////////////////////////////////

#include <system/Serialisable.h>
#include <system/SerialisationIds.h>

///////////////////////////////////////////////////////////////////////////////
// Local Includes
///////////////////////////////////////////////////////////////////////////////

using namespace std;

///////////////////////////////////////////////////////////////////////////////
// Classes
///////////////////////////////////////////////////////////////////////////////

class EventNotify : public threeway::Serialisable
{
public:
    /**
     * Construction / destruction.
     */
    EventNotify() {}
    virtual ~EventNotify() {};

    virtual u32 GetSerialisationId() const { return SERIALISATION_ID_EVENT_NOTIFY; }
    virtual s32 Serialise(u8* data, u32 dataMaxBytes) const { return 1; }
    virtual bool DeSerialise(const u8* data, u32 dataLen) { return true; }

    virtual std::string ToString() const { return "EventNotify"; }

private:
};

#endif
