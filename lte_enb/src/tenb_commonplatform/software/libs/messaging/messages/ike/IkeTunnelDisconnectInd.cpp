using namespace std;
using namespace threeway;

s32 IkeTunnelDisconnectInd::Serialise(u8* data, u32 dataMaxBytes) const
{
    ENTER();

    u8 * start = data;

    if (4 <= dataMaxBytes)
    {
        SerialiseIt(m_id, data);
    }

    RETURN(data - start);
}

bool IkeTunnelDisconnectInd::DeSerialise(const u8* data, u32 dataLen)
{
    ENTER();

    bool decode = (4 <= dataLen);

    if (decode)
    {
        DeSerialiseIt(m_id, data);
    }

    RETURN(decode);
}

string IkeTunnelDisconnectInd::ToString() const
{
    ENTER();

    ostringstream stream;

    stream << "IkeTunnelDisconnectInd:" << " id=" << m_id;

    RETURN(stream.str());
}
