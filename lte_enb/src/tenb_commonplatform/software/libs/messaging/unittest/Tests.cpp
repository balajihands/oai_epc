#include <cppunit/TestSuite.h>

///////////////////////////////////////////////////////////////////////////////
// Local Includes
///////////////////////////////////////////////////////////////////////////////

#include "UdpMessengerTest.h"
#include "UdpMessengerPolledTest.h"
#include "MessageTests.h"

///////////////////////////////////////////////////////////////////////////////
// Functions
///////////////////////////////////////////////////////////////////////////////

extern "C" CppUnit::Test* GetTests();
CppUnit::Test* GetTests()
{
    CppUnit::TestSuite* suite = new CppUnit::TestSuite("Messaging library unit tests");

    suite->addTest( new UdpMessengerTest() );
    suite->addTest( new UdpMessengerPolledTest() );
    suite->addTest( new MessageUnitTests() );
    
    return suite;
}
