void textColour (TextAttr textAttr, TextColour fgColour, TextColour bgColour)
{
	// Command is the control command to the terminal.
	char commandText[32];
	snprintf(commandText, sizeof(commandText),
	         "%c[%d;%d;%dm", 0x1B, textAttr, fgColour + 30, bgColour + 40);
	printf("%s", commandText);
}

